public class Callerinformation {
    String name,lastname, description;

    public  Callerinformation(String Name, String LastName, String Description){
        name = Name;
        lastname = LastName;
        description = Description;

    }

    public String getName() {
        return name;
    }

    public String getLastname() {

        return lastname;
    }

    public String getDescription() {

        return description;
    }
}
